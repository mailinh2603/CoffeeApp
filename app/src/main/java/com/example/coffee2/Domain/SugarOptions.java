package com.example.coffee2.Domain;

public class SugarOptions {
    private int Id;
    private String Name;

    public SugarOptions() {
    }

    public int getId() {
        return Id;
    }

    public void setId(int id) {
        this.Id = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        this.Name = name;
    }
}
